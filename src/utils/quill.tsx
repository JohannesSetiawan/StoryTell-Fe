import { Quill } from "react-quill";

// Custom Undo button icon component for Quill editor. You can import it directly
// from 'quill/assets/icons/undo.svg' but I found that a number of loaders do not
// handle them correctly


// Add sizes to whitelist and register them
const Size = Quill.import("formats/size");
Size.whitelist = ["extra-small", "small", "medium", "large"];
Quill.register(Size, true);

// Add fonts to whitelist and register them
const Font = Quill.import("formats/font");
Font.whitelist = [
  "arial",
  "comic-sans",
  "courier-new",
  "georgia",
  "helvetica",
  "lucida"
];
Quill.register(Font, true);

export const modules = {
  toolbar: {
    container: "#toolbar", // Bind the custom toolbar to a DOM element (usually an ID)
  },
  history: {
    delay: 500,      // Delay between history stack updates (in ms)
    maxStack: 100,   // Maximum size of undo/redo history stack
    userOnly: true,  // Only track changes made by the user
  },
};

